            f'<div class="softude-cta-wrap" style="margin-top:26px;">'
            f'<a class="softude-cta" href="{escape(cta_url, quote=True)}" '
            f'style="display:inline-block;background:{accent};color:#ffffff;text-decoration:none;'
            f'padding:12px 20px;border-radius:8px;font-size:14px;font-weight:700;">'
            f"{escape(cta_label)}</a></div>"
