def build_intake_owner_email(
    *,
    category: str,
    submitted_by: str,
    request_text: str,
    external_submission_id: str,
    submission_id: str,
    monday: dict[str, Any] | None = None,
) -> tuple[str, str, str]:
    """Return ``(subject, body_text, body_html)`` for Intake category-owner notify."""
    monday = monday or {}
    board = monday.get("board") if isinstance(monday.get("board"), dict) else {}
    monday_url = str(board.get("url") or "").strip()
    category_label = category.replace("_", " ").title()
    subject = f"[Intake] {category_label} · {external_submission_id}"

    preview = request_text.strip()
    if len(preview) > 280:
        preview = preview[:277].rstrip() + "..."

    console = dashboard_url()
    detail_url = f"{console}/intake-agent/{submission_id}"
    cta_url = monday_url or detail_url
    cta_label = "Open in Monday" if monday_url else "Review in Ops Console"

    body_text = (
        f"New Intake assignment — {category_label}\n\n"
        f"A client intake was routed to your category by Softude AI.\n\n"
        f"Category: {category_label}\n"
        f"Submitted by: {submitted_by}\n"
        f"Submission ID: {external_submission_id}\n"
        f"Request:\n{request_text}\n\n"
        f"Open: {cta_url}\n\n"
        f"This notification was generated by Softude Ops Console."
    )

    intro_html = (
        "A client intake was classified and assigned to your category by "
        f"<strong>{escape(product_name())}</strong>."
    )
    body_html = render_branded_email(
        eyebrow="Intake Agent",
        title=f"{category_label} assigned",
        intro_html=intro_html,
        badge=category_label,
        rows=[
            ("Category", category_label),
            ("Submitted by", submitted_by),
            ("Submission ID", external_submission_id),
            ("Request", preview),
        ],
        cta_label=cta_label,
        cta_url=cta_url,
        footer_note=(
            "You received this because you are the configured owner for this "
            "Intake category in Softude Ops Console."
        ),
    )
    return subject, body_text, body_html


