        logo_html = (
            f'<img class="softude-logo" src="{escape(logo, quote=True)}" alt="{brand}" '
            'width="140" height="32" '
            'style="display:block;border:0;margin-bottom:14px;max-width:140px;height:auto;" />'
        )
