            '<div class="softude-logo" style="width:36px;height:36px;border-radius:10px;'
            'background:linear-gradient(135deg,#14b8a6,#0ea5e9);'
            'color:#041016;font-size:14px;font-weight:800;letter-spacing:-0.5px;line-height:36px;'
            'text-align:center;margin-bottom:12px;">AI</div>'
